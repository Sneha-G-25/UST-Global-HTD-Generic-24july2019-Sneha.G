public class Arithmatic
{
	public static void main(String[] args) 
	{
		int i = 10 ;
		int j = 20 ;
		int res ;
		int x =(int) 'z';

		System.out.println(x);

		res = i + j ;   // re-initialization
		System.out.println(" + res = " + res) ;

		res = i - j ;   // re-initialization
		System.out.println(" - res = " + res) ;

		res = i / j ;   // re-initialization
		System.out.println(" / res  = " + res) ;

		res = i * j ;
		System.out.println(" * res = " + res) ;

	}
}