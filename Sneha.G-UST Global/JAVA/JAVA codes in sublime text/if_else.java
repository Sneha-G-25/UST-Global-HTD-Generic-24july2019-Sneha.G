public class if_else {
	public static void main(String[] args)
	 {
		byte  i = 20;
		byte j = 10 ;
		 if (i> j)		// boolean condition with if condition
		 {
		 	System.out.println("i is greater than j");		// if-block
		 }
		 else 
		 {
		 	System.out.println("j is greater than i"); 	// else-blocl
		 }
		 	System.out.println("out-side code"); // code out side the block
	}
}